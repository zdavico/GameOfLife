// ============================================================================
// WATERFALL CHART COMPONENT
// ============================================================================
// Renders a waterfall (cascade) chart showing how income decreases step by
// step as each budget category is subtracted. The first bar is the full
// effective income, each subsequent bar shows a category's deduction, and
// the final bar shows the remaining balance.
//
// Color coding:
//   - Income bar uses the accent color
//   - Category bars are colored by their dominant NWG type (needs, wants,
//     goals, or "mixed" for split categories like food or debt)
//   - Remaining bar is green if positive, red if over budget
//   - If the remaining amount falls within the tolerance band, a checkmark
//     "within tolerance" label is shown instead of the dollar amount
//
// Dashed connector lines link the top of each bar to the next, showing the
// running total's path down.
//
// Props: same as SankeyDiagram, plus `tolerance` (the ±% threshold)
// ============================================================================
function WaterfallChart({ monthlyNet, wheelOfLife, budgetValues, analysis, t, expanded, tolerance = 5 }) {
  const effectiveIncome = monthlyNet + (wheelOfLife || 0);
  if (effectiveIncome <= 0) return <div style={{ color: t.textMuted, textAlign: "center", padding: 60, fontSize: 13 }}>Enter a student to see budget waterfall</div>;

  const cats = CATEGORIES.filter(c => c.key !== "wheelOfLife");
  const activeCats = cats.filter(c => (budgetValues[c.key] || 0) > 0);
  if (activeCats.length === 0) return <div style={{ color: t.textMuted, textAlign: "center", padding: 60, fontSize: 13 }}>Enter budget values to see the waterfall</div>;

  // Build the waterfall steps:
  // Step 1: Income (positive, sets the starting point)
  // Steps 2–N: Each active category (negative values, decreasing the running total)
  // Final step: Remaining balance (can be positive or negative)
  const steps = [{ label: "Income", value: effectiveIncome, type: "income", running: effectiveIncome }];
  let running = effectiveIncome;
  for (const c of activeCats) {
    const val = budgetValues[c.key] || 0;
    const catDef = CATEGORIES.find(x => x.key === c.key);
    // Determine the dominant NWG type for color coding
    let nwgType = "needs";
    if (catDef?.alloc) {
      if (catDef.alloc.wants >= catDef.alloc.needs && catDef.alloc.wants >= catDef.alloc.goals) nwgType = "wants";
      if (catDef.alloc.goals >= catDef.alloc.needs && catDef.alloc.goals >= catDef.alloc.wants) nwgType = "goals";
      if (catDef.alloc.needs === 0.5 && catDef.alloc.wants === 0.5) nwgType = "mixed-nw";   // e.g., Food, Personal Care
      if (catDef.alloc.needs === 0.5 && catDef.alloc.goals === 0.5) nwgType = "mixed-ng";    // e.g., Debt Management
    }
    running -= val;
    steps.push({ label: c.label.split(" ")[0], value: -val, type: nwgType, color: CAT_COLORS[CATEGORIES.findIndex(x => x.key === c.key)], running });
  }
  steps.push({ label: "Remaining", value: running, type: "remaining", running });

  // Tolerance band: if the remaining balance is within this dollar amount
  // of zero, it's considered "balanced" and gets a checkmark instead of
  // showing the exact dollar amount.
  const toleranceBand = effectiveIncome * (tolerance / 100);

  // ── SVG layout calculations ──
  const PAD_L = 60, PAD_R = 20, PAD_T = 36, PAD_B = 50,
    barGap = 4,
    width = expanded ? 800 : 540,
    height = expanded ? 480 : 300;
  const chartW = width - PAD_L - PAD_R, chartH = height - PAD_T - PAD_B;
  const barW = Math.min(36, (chartW - barGap * (steps.length - 1)) / steps.length);
  const totalBarSpace = barW * steps.length + barGap * (steps.length - 1);
  const offsetX = PAD_L + (chartW - totalBarSpace) / 2; // Center bars horizontally

  // Y-axis scale: maps dollar values to SVG y-coordinates
  const maxVal = effectiveIncome * 1.05,
    minVal = Math.min(0, running) * 1.1,
    range = maxVal - minVal;
  const yScale = (v) => PAD_T + (1 - (v - minVal) / range) * chartH;

  /**
   * getColor(step) — returns the fill color for a waterfall bar based on
   * its type (income, remaining, needs, wants, goals, or mixed).
   */
  function getColor(s) {
    if (s.type === "income") return t.accent;
    if (s.type === "remaining") return s.value >= 0 ? t.success : t.danger;
    if (s.type === "needs") return t.needs;
    if (s.type === "wants") return t.wants;
    if (s.type === "goals") return t.goals;
    return "#7A87A8"; // Mixed categories
  }

  // Pre-compute bar positions. Income and Remaining bars grow from 0.
  // Category bars "hang" from the previous running total down to the new one.
  const bars = steps.map((step, i) => {
    const x = offsetX + i * (barW + barGap);
    if (step.type === "income" || step.type === "remaining") {
      const top = yScale(Math.max(0, step.value)), bottom = yScale(Math.min(0, step.value));
      return { ...step, x, top, bottom, barH: bottom - top };
    }
    const runBefore = step.running - step.value; // Running total BEFORE this deduction
    const top = yScale(runBefore), bottom = yScale(step.running);
    return { ...step, x, top: Math.min(top, bottom), bottom: Math.max(top, bottom), barH: Math.abs(bottom - top), runBefore };
  });

  // Y-axis tick marks
  const tickCount = 5, tickStep = range / tickCount;
  const ticks = Array.from({ length: tickCount + 1 }, (_, i) => {
    const val = minVal + tickStep * i;
    return { val, y: yScale(val) };
  });

  return (
    <svg viewBox={`0 0 ${width} ${height}`} width="100%" style={expanded ? {} : { maxHeight: 320 }}>
      {/* Y-axis grid lines and tick labels */}
      {ticks.map((tk, i) => (<g key={i}><line x1={PAD_L-5} y1={tk.y} x2={width-PAD_R} y2={tk.y} stroke={t.border} strokeWidth={0.5} strokeDasharray={tk.val===0?"none":"3 3"} opacity={tk.val===0?0.6:0.3}/><text x={PAD_L-8} y={tk.y+3} textAnchor="end" fill={t.textMuted} fontSize={9} fontFamily="'JetBrains Mono', monospace">{fmt(tk.val)}</text></g>))}

      {/* Dashed connector lines between bars showing the running total path */}
      {bars.map((bar, i) => { if (i === 0 || bar.type === "remaining") return null; const connY = bar.runBefore !== undefined ? yScale(bar.runBefore) : bars[i-1].top; return <line key={`cn${i}`} x1={bars[i-1].x+barW} y1={connY} x2={bar.x} y2={connY} stroke={t.textMuted} strokeWidth={1} strokeDasharray="2 2" opacity={0.4}/>; })}

      {/* Bar rectangles with value labels and category names */}
      {bars.map((bar, i) => {
        const color = getColor(bar);
        const isEnd = bar.type === "income" || bar.type === "remaining";
        // Check if the remaining bar is within tolerance
        const withinTol = bar.type === "remaining" && Math.abs(bar.value) <= toleranceBand && analysis.totalExpenses > 0;
        const labelText = withinTol ? null : `${bar.value < 0 ? "-" : ""}${fmt(Math.abs(bar.value))}`;
        return (<g key={`br${i}`}>
          <rect x={bar.x} y={bar.top} width={barW} height={Math.max(2, bar.barH)} rx={3} fill={color} opacity={isEnd ? 0.92 : 0.78}/>
          {/* Show "✓ within tolerance" for balanced budgets, otherwise show dollar amount */}
          {withinTol
            ? <><text x={bar.x+barW/2} y={bar.top-14} textAnchor="middle" fill={color} fontSize={8} fontWeight={700}>{"\u2713"} within</text><text x={bar.x+barW/2} y={bar.top-5} textAnchor="middle" fill={color} fontSize={8} fontWeight={700}>tolerance</text></>
            : <text x={bar.x+barW/2} y={bar.top-6} textAnchor="middle" fill={color} fontSize={9} fontWeight={700} fontFamily="'JetBrains Mono', monospace">{labelText}</text>}
          {/* Category label below the x-axis (rotated if many bars) */}
          <text x={bar.x+barW/2} y={height-PAD_B+14} textAnchor="middle" fill={t.textDim} fontSize={8} fontWeight={500} transform={steps.length>8?`rotate(-35 ${bar.x+barW/2} ${height-PAD_B+14})`:""}>  {bar.label}</text>
        </g>);
      })}

      {/* Legend: NWG color key at the bottom of the chart */}
      {[{l:"Needs",c:t.needs,x:PAD_L},{l:"Wants",c:t.wants,x:PAD_L+60},{l:"Goals",c:t.goals,x:PAD_L+120},{l:"Mixed",c:"#7A87A8",x:PAD_L+180}].map(l=>(<g key={l.l}><rect x={l.x} y={height-12} width={8} height={8} rx={2} fill={l.c} opacity={0.8}/><text x={l.x+12} y={height-4.5} fill={t.textMuted} fontSize={8}>{l.l}</text></g>))}
    </svg>
  );
}


// ============================================================================
// THERMOMETER COMPONENT
// ============================================================================
// A vertical thermometer-style gauge that shows what percentage of effective
// income has been allocated. Sits alongside the main visualization panel.
//
// Visual states:
//   - Fill height proportional to allocated/income ratio (capped at 100% visually)
//   - Red ("OVER BUDGET") when spending exceeds income + tolerance band
//   - Green ("BALANCED" or "ZERO-BASED!") when within tolerance of zero remaining
//   - Accent color with "X left" when under-allocated
//   - Tick marks at 50%, 80%, and 100% for reference
//
// Props:
//   allocated    – total dollars allocated across all categories
//   monthlyNet   – base monthly net income
//   wheelOfLife  – Wheel of Fortune adjustment
//   t            – current theme object
//   tolerance    – the ±% balance tolerance setting
// ============================================================================
function Thermometer({ allocated, monthlyNet, wheelOfLife, t, tolerance = 5 }) {
  const effectiveIncome = monthlyNet + (wheelOfLife || 0);
  if (effectiveIncome <= 0) return null;

  const pctAllocated = Math.min(allocated / effectiveIncome, 1.5); // Allow visual overflow to 150%
  const remaining = effectiveIncome - allocated;
  const toleranceBand = effectiveIncome * (tolerance / 100);

  // Determine status based on remaining balance vs. tolerance band
  const isOver = remaining < -toleranceBand;
  const isBalanced = allocated > 0 && Math.abs(remaining) <= toleranceBand;
  const isZero = Math.abs(remaining) < 1 && allocated > 0; // Exactly zero-based

  const fillColor = isOver ? t.danger : isBalanced ? t.success : t.accent;
  const fillHeight = Math.min(pctAllocated, 1.0) * 100; // Visual fill capped at 100%
  const statusText = isOver ? "OVER BUDGET" : isZero ? "ZERO-BASED!" : isBalanced ? "BALANCED" : remaining > 0 ? `${fmt(remaining)} left` : "";

  return (
    <div style={{ display: "flex", flexDirection: "column", alignItems: "center", gap: 8, padding: "12px 0" }}>
      <div style={{ fontSize: 11, fontWeight: 700, color: t.textDim, letterSpacing: 1, textTransform: "uppercase" }}>Allocation</div>
      {/* Thermometer body */}
      <div style={{ position: "relative", width: 48, height: 180, background: t.inputBg, borderRadius: 24, border: `2px solid ${t.border}`, overflow: "hidden" }}>
        {/* Animated fill that rises from the bottom */}
        <div style={{ position: "absolute", bottom: 0, left: 0, right: 0, height: `${fillHeight}%`, background: `linear-gradient(to top, ${fillColor}dd, ${fillColor}88)`, borderRadius: "0 0 22px 22px", transition: "height 0.6s cubic-bezier(0.34, 1.56, 0.64, 1), background 0.3s" }}/>
        {/* Percentage label floating inside the fill */}
        <div style={{ position: "absolute", bottom: 0, left: 0, right: 0, height: `${fillHeight}%`, minHeight: fillHeight > 5 ? 30 : 0, display: "flex", alignItems: "center", justifyContent: "center" }}>
          {fillHeight > 10 && <span style={{ color: "#fff", fontSize: 12, fontWeight: 800, background: fillColor, padding: "2px 6px", borderRadius: 10, zIndex: 2, position: "relative", boxShadow: "0 1px 4px #0004" }}>{(pctAllocated * 100).toFixed(0)}%</span>}
        </div>
        {/* Reference tick marks at 50%, 80%, and 100% */}
        {[0.5, 0.8, 1.0].map(v => (
          <div key={v} style={{ position: "absolute", bottom: `${v * 100}%`, left: 4, right: 4, height: 1, background: v === 1 ? t.success : t.textMuted, opacity: 0.5 }}>
            <span style={{ position: "absolute", right: -28, top: -6, fontSize: 8, color: t.textMuted }}>{(v*100)}%</span>
          </div>
        ))}
      </div>
      {/* Status text below the thermometer */}
      <div style={{ fontSize: 12, fontWeight: 700, color: isOver ? t.danger : isBalanced ? t.success : t.textDim, textAlign: "center" }}>{statusText}</div>
      <div style={{ fontSize: 10, color: t.textMuted }}>{fmt(allocated)} of {fmt(effectiveIncome)}</div>
    </div>
  );
}


