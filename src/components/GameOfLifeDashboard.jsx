// ============================================================================
// SUGGESTED WEIGHT PRESETS
// ============================================================================
// Two built-in presets that define the "suggested %" baseline for each
// category. These are used by:
//   1. The coaching prompts (to determine over/under thresholds)
//   2. The radar chart (the "suggested" polygon)
//   3. The category weights modal (preset buttons)
//
// DEFAULT_WEIGHTS ("Realistic First-Year"):
//   Reflects typical spending patterns for a recent graduate in their first
//   year of full-time employment, with a heavier housing allocation and
//   moderate savings.
//
// OPTIMIZED_WEIGHTS ("50/30/20 Optimized"):
//   A more aspirational split that tilts toward the textbook 50/30/20
//   framework, with lower housing and higher leisure/savings.
//
// Coaches can also create a "Custom" preset by adjusting individual sliders
// in the weights modal. Custom weights persist in localStorage.
// ============================================================================
const DEFAULT_WEIGHTS = { housing: 28, food: 12, utilities: 8, transportation: 10, personalCare: 5, leisure: 8, savings: 15, pets: 2, debt: 8, industry: 4 };
const OPTIMIZED_WEIGHTS = { housing: 22, food: 14, utilities: 5, transportation: 6, personalCare: 8, leisure: 16, savings: 16, pets: 3, debt: 8, industry: 2 };
const PRESETS = [
  { key: "realistic", label: "Realistic First-Year", weights: DEFAULT_WEIGHTS },
  { key: "optimized", label: "50/30/20 Optimized", weights: OPTIMIZED_WEIGHTS },
];

// WEIGHT_KEYS: The list of category keys that participate in weight presets.
// Wheel of Fortune is excluded because it doesn't have a suggested % — it's
// an income modifier, not a spending category.
const WEIGHT_KEYS = CATEGORIES.filter(c => c.key !== "wheelOfLife").map(c => c.key);

/**
 * computeNWG(weights)
 * Given a set of category weight percentages (e.g., { housing: 28, food: 12, ... }),
 * compute the aggregate Needs / Wants / Goals split by multiplying each
 * category's weight by its alloc fractions and summing across all categories.
 *
 * Example: If housing = 28% and housing.alloc.needs = 1.0, that contributes
 * 28% to needs. If food = 12% and food.alloc = { needs: 0.5, wants: 0.5 },
 * that contributes 6% to needs and 6% to wants.
 *
 * Returns: { needs: number, wants: number, goals: number } (each rounded to 1 decimal)
 */
function computeNWG(weights) {
  let n = 0, w = 0, g = 0;
  for (const cat of CATEGORIES) { if (!cat.alloc || cat.key === "wheelOfLife") continue; const v = weights[cat.key] || 0; n += v * cat.alloc.needs; w += v * cat.alloc.wants; g += v * cat.alloc.goals; }
  return { needs: Math.round(n * 10) / 10, wants: Math.round(w * 10) / 10, goals: Math.round(g * 10) / 10 };
}

/**
 * identifyPreset(weights)
 * Checks whether a given set of weights exactly matches one of the built-in
 * presets. Returns the preset label string (e.g., "Realistic First-Year") if
 * there's a match, or "Custom" if the weights have been manually adjusted.
 * This is displayed in the settings panel and weights modal.
 */
function identifyPreset(weights) {
  for (const p of PRESETS) { if (WEIGHT_KEYS.every(k => (weights[k] || 0) === p.weights[k])) return p.label; }
  return "Custom";
}

// ── localStorage persistence for custom weights ──
// The coach's chosen weight configuration persists across browser sessions
// so they don't have to reconfigure every time. The key "gol-suggested-weights"
// stores the full weights object as JSON.
const LS_KEY = "gol-suggested-weights";

/**
 * loadWeights()
 * Attempts to load saved weights from localStorage. If the stored value is
 * valid (all expected keys present with numeric values), it's returned.
 * Otherwise, falls back to DEFAULT_WEIGHTS.
 */
function loadWeights() { try { const raw = localStorage.getItem(LS_KEY); if (raw) { const parsed = JSON.parse(raw); if (WEIGHT_KEYS.every(k => typeof parsed[k] === "number")) return parsed; } } catch {} return DEFAULT_WEIGHTS; }

/**
 * saveWeights(w)
 * Persists the current weights object to localStorage. Wrapped in try/catch
 * to gracefully handle storage quota or privacy errors.
 */
function saveWeights(w) { try { localStorage.setItem(LS_KEY, JSON.stringify(w)); } catch {} }


