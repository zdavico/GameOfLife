// ============================================================================
// GameOfLifeDashboard.jsx
// ============================================================================
// The Game of Life Budget Dashboard is a React-based coaching tool used by
// peer coaches ("money managers") during Champlain College's InSight program
// financial simulation event. Students are assigned a fictional salary and
// asked to build a monthly budget across real-world categories. Coaches use
// this dashboard to look up students, enter budget values in real time,
// visualize how the budget maps to the 50/30/20 framework (Needs / Wants /
// Goals), surface coaching prompts when allocations drift from suggested
// baselines, and log each session for aggregate analysis.
//
// Key features:
//   - Student lookup from a pre-built JSON dataset (built via npm run build-data)
//   - Budget entry with per-category inputs and live NWG (Needs/Wants/Goals) analysis
//   - What-If slider mode for scenario exploration
//   - Multiple visualization modes: dual donut comparison, Sankey income-flow
//     diagram, waterfall chart, and radar overlay
//   - Coaching prompts triggered when a category is over or under its baseline
//   - Configurable suggested-weight presets (Realistic First-Year vs. 50/30/20)
//   - Aggregate dashboard tracking session-level stats across multiple students
//   - Print-friendly summary modal
//   - Light ("Champlain Colors") and dark theme support
//   - Adjustable font scale and balance tolerance
//
// Data pipeline: The master student roster lives in data/master.csv. Running
// `npm run build-data` converts it to data/students.json, which is imported
// here at build time. The app ships with 12 sample students so it works
// immediately without the full CSV.
// ============================================================================

